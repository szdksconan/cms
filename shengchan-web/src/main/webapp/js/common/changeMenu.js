function changeMenu(datas,sid,id){
		var innerHtml ='';
		for(var i=0;i<datas.length;i++){
			var data = datas[i];
			var list = data.list;
			if(list.length > 0){
				innerHtml += '<div class="subNav" onclick=\"changeContent(\''+data.url+'\',\''+sid+'\')\">'+data.name+'</div>';
					if(i==0){
						innerHtml += '   <ul class=\"navContent\" style="display:block">';
					}else{
						innerHtml += '   <ul class=\"navContent\">';
					}
				for( var j=0;j<list.length;j++){
					var child = list[j];
					innerHtml += '   <li><a onclick=\"changeContent(\''+child.url+'\',\''+sid+'\')\" href=\"javascript:void(0);\">'+child.name+'</a></li>'	;
				}
				innerHtml += '   </ul>'	;
			}else{
				innerHtml += '<div class="subNav" onclick=\"changeContent(\''+data.url+'\',\''+sid+'\')\">'+data.name+'</div>';
			}
		}
		$('#'+id).html(innerHtml);
}
function changeContent(url,id){
	if(url != ''){
		$('#'+id).load(url);
	}
}